import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const sourcePath = path.join(repoRoot, 'src', 'config', 'deploy-services.json');
const workflowPath = path.join(repoRoot, '.github', 'workflows', 'deploy.yml');

function indent(lines, prefix = '          ') {
  return lines.map((line) => `${prefix}${line}`).join('\n');
}

function yamlList(values) {
  return values.map((value) => `- ${value}`);
}

function isDeployEnvironment(value) {
  return value === 'staging' || value === 'prod';
}

function validateServiceConfig(service, seenNames) {
  if (!service || typeof service !== 'object') {
    throw new Error('each service must be an object');
  }

  if (typeof service.name !== 'string' || service.name.trim() === '') {
    throw new Error('each service must have a non-empty name');
  }

  if (seenNames.has(service.name)) {
    throw new Error(`duplicate deploy service: ${service.name}`);
  }
  seenNames.add(service.name);

  if (
    !Array.isArray(service.allowed_environments) ||
    service.allowed_environments.length === 0
  ) {
    throw new Error(`service ${service.name} must have allowed_environments`);
  }

  for (const environment of service.allowed_environments) {
    if (!isDeployEnvironment(environment)) {
      throw new Error(
        `service ${service.name} has invalid environment ${environment}`
      );
    }
  }
}

function validateConfig(config) {
  if (!config || typeof config !== 'object') {
    throw new Error('deploy-services.json must contain an object');
  }

  const {
    default_environment: defaultEnvironment,
    default_service: defaultService,
    services
  } = config;

  if (!isDeployEnvironment(defaultEnvironment)) {
    throw new Error('default_environment must be staging or prod');
  }

  if (!Array.isArray(services) || services.length === 0) {
    throw new Error('services must be a non-empty array');
  }

  const seenNames = new Set();
  for (const service of services) {
    validateServiceConfig(service, seenNames);
  }

  if (!seenNames.has(defaultService)) {
    throw new Error(`default_service ${defaultService} is not in services`);
  }
}

function buildWorkflowYaml(config) {
  const serviceNames = config.services.map((service) => service.name);
  const prodOnlyServices = config.services
    .filter(
      (service) =>
        service.allowed_environments.length === 1 &&
        service.allowed_environments[0] === 'prod'
    )
    .map((service) => service.name);
  const stagingOnlyServices = config.services
    .filter(
      (service) =>
        service.allowed_environments.length === 1 &&
        service.allowed_environments[0] === 'staging'
    )
    .map((service) => service.name);

  const prodCondition = prodOnlyServices.length
    ? `(${prodOnlyServices
        .map(
          (service) => `github.event.inputs.service == '${service}'`
        )
        .join(' || ')}) && github.event.inputs.environment != 'prod'`
    : 'false';
  const stagingCondition = stagingOnlyServices.length
    ? `(${stagingOnlyServices
        .map(
          (service) => `github.event.inputs.service == '${service}'`
        )
        .join(' || ')}) && github.event.inputs.environment != 'staging'`
    : 'false';

  return `# This file is generated by scripts/generate-deploy-config.mjs.
# Edit src/config/deploy-services.json and rerun the generator instead.
name: Deploy a service

on:
  workflow_dispatch:
    inputs:
      environment:
        type: choice
        description: 'Environment'
        required: true
        default: ${config.default_environment}
        options:
${indent(yamlList(['staging', 'prod']))}
      service:
        type: choice
        description: 'Service'
        required: true
        default: ${config.default_service}
        options:
${indent(yamlList(serviceNames))}

env:
  SENTRY_DSN: \${{ secrets.SENTRY_DSN }}
  SENTRY_AUTH_TOKEN: \${{ secrets.SENTRY_AUTH_TOKEN }}

run-name: Deploy \${{ github.event.inputs.service }} to \${{ github.event.inputs.environment }}

jobs:
  build-and-deploy:
    name: Build and deploy \${{ github.event.inputs.service }} to \${{ github.event.inputs.environment }}
    runs-on: ubuntu-latest
    steps:
      - name: Check production preconditions
        uses: actions/github-script@v3
        if: ${prodCondition}
        with:
          script: core.setFailed('Given service can only be deployed to production environment')
      - name: Check staging preconditions
        uses: actions/github-script@v3
        if: ${stagingCondition}
        with:
          script: core.setFailed('Given service can only be deployed to staging environment')
      - name: Extract branch name
        shell: bash
        run: echo "branch=\${GITHUB_HEAD_REF:-\${GITHUB_REF#refs/heads/}}" >> $GITHUB_OUTPUT
        id: extract_branch
      - name: Checkout
        uses: actions/checkout@v3
        with:
          ref: \${{ steps.extract_branch.outputs.branch }}
      - name: Install root dependencies
        run: npm i
      - name: Install lambda dependencies
        if: github.event.inputs.service != 'api'
        run: npm i && pushd src/\${{ github.event.inputs.service }} && npm i && popd
      - name: Install api dependencies
        if: github.event.inputs.service == 'api'
        run: pushd src/api-serverless && npm i && popd
      - name: Build service
        if: github.event.inputs.service != 'api'
        run: pushd src/\${{ github.event.inputs.service }} && npm run build && popd
      - name: Build API
        if: github.event.inputs.service == 'api'
        run: pushd src/api-serverless && npm run build && popd
      - name: Configure AWS credentials
        uses: aws-actions/configure-aws-credentials@13d241b293754004c80624b5567555c4a39ffbe3
        with:
          aws-access-key-id: \${{ secrets.AWS_ACCESS_KEY_ID }}
          aws-secret-access-key: \${{ secrets.AWS_SECRET_ACCESS_KEY }}
          aws-region: \${{ github.event.inputs.environment == 'prod' && 'us-east-1' || 'eu-west-1' }}
      - name: Deploy service
        if: github.event.inputs.service != 'api' && github.event.inputs.service != 'nextgenMediaProxyInterceptor' && github.event.inputs.service != 'mediaResizerLoop'
        run: |
          export VERSION_DESCRIPTION="$(date) - $(git rev-parse --abbrev-ref HEAD) - $(git show -s --format=%s)"
          pushd src/\${{ github.event.inputs.service }} && npm run sls-deploy:\${{ github.event.inputs.environment }} && popd
      - name: Deploy API
        if: github.event.inputs.service == 'api'
        run: |
          set +x
          GIT_COMMIT=$(git rev-parse HEAD)
          DEPLOY_REGION="\${{ github.event.inputs.environment == 'prod' && 'us-east-1' || 'eu-west-1' }}"
          API_MEMORY_SIZE=2048
          API_TIMEOUT=30
          CLAIMS_MEDIA_ARWEAVE_UPLOAD_SQS_URL="https://sqs.\${DEPLOY_REGION}.amazonaws.com/987989283142/claims-media-arweave-upload"
          aws lambda update-function-code --function-name seizeAPI --zip-file fileb://src/api-serverless/dist/index.zip --no-cli-pager > /dev/null 2>&1
          sleep 10
          aws lambda get-function-configuration --function-name seizeAPI --query 'Environment.Variables' --output json --no-cli-pager > /tmp/current_env.json 2>/dev/null || echo '{}' > /tmp/current_env.json
          jq --arg commit "$GIT_COMMIT" --arg claimsMediaArweaveUploadSqsUrl "$CLAIMS_MEDIA_ARWEAVE_UPLOAD_SQS_URL" '. + {GIT_COMMIT: $commit, CLAIMS_MEDIA_ARWEAVE_UPLOAD_SQS_URL: $claimsMediaArweaveUploadSqsUrl} | {Variables: .}' /tmp/current_env.json > /tmp/env_config.json
          aws lambda update-function-configuration --function-name seizeAPI --description "$(date) - $(git rev-parse --abbrev-ref HEAD) - $(git show -s --format=%s)" --environment file:///tmp/env_config.json --memory-size "$API_MEMORY_SIZE" --timeout "$API_TIMEOUT" --no-cli-pager > /dev/null 2>&1
          rm -f /tmp/current_env.json /tmp/env_config.json
      - name: Deploy mediaResizerLoop
        if: github.event.inputs.service == 'mediaResizerLoop'
        run: |
          aws lambda update-function-code --function-name  mediaResizerLoop --zip-file fileb://src/mediaResizerLoop/dist/index.zip
          sleep 10
          aws lambda update-function-configuration --function-name mediaResizerLoop --description "$(date) - $(git rev-parse --abbrev-ref HEAD) - $(git show -s --format=%s)"
      - name: Deploy nextgenMediaProxyInterceptor
        if: github.event.inputs.service == 'nextgenMediaProxyInterceptor'
        run: |
          pushd src/nextgenMediaProxyInterceptor
          sh deploy.sh
          popd
      - name: Run lambda and validate result
        if: github.event.inputs.service == 'dbMigrationsLoop'
        run: |
          sleep 10

          aws lambda invoke --function-name \${{ github.event.inputs.service }} response.json > meta.json

          printf "\\nresponse.json:\\n"
          cat response.json
          printf "\\n\\nmeta.json:\\n"
          cat meta.json
          printf "\\n"

          if jq -e '.FunctionError == "Unhandled"' meta.json > /dev/null; then
            echo "Lambda function threw an unhandled error"
            exit 1
          fi
      - name: Notify about failure
        uses: sarisia/actions-status-discord@v1
        if: failure()
        env:
          DISCORD_WEBHOOK: \${{ secrets.DISCORD_WEBHOOK }}
        with:
          title: Seize-Lambda \${{ github.event.inputs.environment }} \${{ github.event.inputs.service }} DEPLOY CI pipeline is broken!!!
          content: '@everyone'
          color: 0xff0000

      - name: Notify about success
        uses: sarisia/actions-status-discord@v1
        if: success()
        env:
          DISCORD_WEBHOOK: \${{ secrets.DISCORD_WEBHOOK }}
        with:
          title: Seize-Lambda \${{ github.event.inputs.environment }} \${{ github.event.inputs.service }} DEPLOY CI pipeline complete
          color: 0x00ff00
`;
}

async function main() {
  const config = JSON.parse(await fs.readFile(sourcePath, 'utf8'));
  validateConfig(config);

  await fs.writeFile(workflowPath, buildWorkflowYaml(config));
}

await main();
